        <h4>School of Management Sciences situated in Varanasi, Uttar Pradesh </h4><p>is a Business School which is providing a total of 8 courses. Under the Post Graduate domain, it offers M.Com., MBA, and MCA.<br>
             Under the undergraduate domain, it offers B.Com., B.Com Hons., BCA, BBA, and B.A. Hons. Admissions to most of the above-mentioned courses are on the basis of score in the online test conducted by the B-School itself. However, for PG courses MBA and MCA, the applicant is required to appear in the Uttar Pradesh State Entrance Examination (UPSEE) which is conducted by the A.P.J. Abdul Kalam University of Lucknow. The process of application is online as well as offline.

            Like every other college, SMS Varanasi also follows a well-prescribed process for admissions to its courses. The Business school conducts proper counseling for these courses:
            
            MBA aspirants must appear in the UPSEE and obtain a valid percentile to be eligible to sit for the counseling round.
            Likewise, MCA aspirants are required to appear in the state examination, i.e., UPSEE and obtain a valid percentile to be able to sit in the counseling session.
            Admissions to all the other 5 UG courses and 1 PG course is strictly based upon the score obtained in the online test.
            The college reserves 15% seats for management quota. It accepts candidate applications on previous exam qualifying marks, CAT, MAT, UPSEE, XAT, CMAT percentile, and personal interview performances.
            School of Management Sciences is maintaining a decent placement record from the last 9 years. Since 2011, the institute has managed to hold the consistency of the salary packages. The highest packages range between INR 6 - 6.5 Lakhs per annum while the average packages range between INR 3 - 3.75 lakhs per annum. The top recruiters include Amul, ColdEx, FedEx, Ayur, ICICI Securities, ITC, DTDC, Airtel, Big Bazaar, Axis Bank, Godrej, DCB Bank, Apollo, Idea, etc.</p><br>     

