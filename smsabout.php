                <h3>Eligibility
                </h3><br>
                <h4>Below is mentioned the eligibility criteria for some of the postgraduate courses imparted at the college:</h4><br>
                <p><h4>M.Com & MBA:</h4> Graduate in any discipline, with minimum 50% aggregate marks (45% for SC/ST category students) from any recognized university.</p><br>
